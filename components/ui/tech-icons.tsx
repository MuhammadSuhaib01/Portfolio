// Icon mapping for technologies
export const getTechIcon = (tech: string) => {
  const techLower = tech.toLowerCase()
  
  switch (techLower) {
    case 'python':
      return <PythonIcon />
    case 'react.js':
    case 'react':
      return <ReactIcon />
    case 'flutter':
      return <FlutterIcon />
    case 'django':
      return <DjangoIcon />
    case 'c#':
      return <CSharpIcon />
    case 'c++':
      return <CPlusPlusIcon />
    case 'yolov8':
    case 'machine learning':
      return <Brain className="w-4 h-4" />
    case 'opencv':
    case 'computer vision':
      return <Eye className="w-4 h-4" />
    case 'firebase':
      return <FirebaseIcon />
    case 'mysql':
      return <MySQLIcon />
    case 'arduino':
      return <ArduinoIcon />
    case 'pyqt5':
      return <Monitor className="w-4 h-4" />
    case 'tailwind css':
      return <TailwindIcon />
    case 'figma':
      return <FigmaIcon />
    default:
      return <Code className="w-4 h-4" />
  }
}
